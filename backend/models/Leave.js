const mongoose = require('mongoose');

const leaveSchema = new mongoose.Schema({
  employee: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'User',
    required: true,
  },
  type: {
    type: String,
    required: true,
  },
  fromDate: {
    type: String,
    required: true,
  },
  toDate: {
    type: String,
    required: true,
  },
  reason: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    enum: ['pending', 'approved', 'rejected'],
    default: 'pending',
  },
  days: {
    type: Number,
    default: 1, // simplified to default 1 for demo purposes
  },
  documentName: {
    type: String,
    default: null
  }
}, { timestamps: true });

module.exports = mongoose.model('Leave', leaveSchema);
